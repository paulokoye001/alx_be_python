#   I = the interest earned,
#   P = the principal amount (initial investment),
#   R = the annual interest rate (as a decimal),
#   T = the time the money is invested for in years.

P = 1000 # in dollars ($)
R = 0.05 # 5%
T = 3 # 3 years

interest = P * R * T

print ('the simple interest ' + str(interest))